#Python program to perform all arithmetic operation

x=int(input("Enter the value of first number :"))
y=int(input("Enter the value of second number :"))

print("Addition of {0} and {1} : {2}".format(x,y,x+y))
print("Subtraction of {0} and {1} : {2}".format(x,y,x-y))
print("Multiplication of {0} and {1} : {2}".format(x,y,x*y))
print("Division of {0} and {1} : {2}".format(x,y,x/y))
print("Modulus of {0} and {1} : {2}".format(x,y,x%y))